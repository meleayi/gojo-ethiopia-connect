import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { supabase } from "@/lib/supabase";
import { useAuth } from "@/contexts/AuthContext";
import type { Database } from "@/lib/database.types";

type OrderStatus = Database["public"]["Enums"]["order_status"];
type DeliveryMethod = Database["public"]["Enums"]["delivery_method"];
type PaymentMethod = Database["public"]["Enums"]["payment_method"];

export interface CreateOrderParams {
  sellerId: string;
  items: {
    productId: string;
    variantId?: string;
    sellerId: string;
    productName: string;
    productImage: string;
    variantName?: string;
    quantity: number;
    unitPrice: number;
  }[];
  shipping: {
    fullName: string;
    phone: string;
    city: string;
    subCity: string;
    woreda: string;
    houseNumber?: string;
    additionalInfo?: string;
    addressId?: string;
  };
  deliveryMethod: DeliveryMethod;
  deliveryFee: number;
  paymentMethod: PaymentMethod;
  notes?: string;
}

export const useOrders = (status?: OrderStatus) => {
  const { user } = useAuth();
  return useQuery({
    queryKey: ["orders", user?.id, status],
    queryFn: async () => {
      if (!user) return [];
      let query = supabase
        .from("orders")
        .select(`
          *,
          order_items(*),
          payments(method, status, amount)
        `)
        .eq("user_id", user.id)
        .order("created_at", { ascending: false });

      if (status) query = query.eq("status", status);

      const { data, error } = await query;
      if (error) throw error;
      return data ?? [];
    },
    enabled: !!user,
  });
};

export const useOrder = (orderId: string) => {
  return useQuery({
    queryKey: ["order", orderId],
    queryFn: async () => {
      const { data, error } = await supabase
        .from("orders")
        .select(`
          *,
          order_items(*, products(name, product_images(url, is_primary))),
          payments(*),
          seller_profiles(company_name, city, is_verified)
        `)
        .eq("id", orderId)
        .single();

      if (error) throw error;
      return data;
    },
    enabled: !!orderId,
  });
};

